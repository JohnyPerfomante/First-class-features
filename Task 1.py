# Створити функцію: def square(x): return x * x. Присвоїти її змінній f, викликати через f(5). Пояснити чому це працює, що саме зберігає змінна f.
def square(x):
    return x * x

f = square

result = f(5)

print(result)  # 25

# Якщо f = square, то відбувається не копіювання коду функції, а передача посилання на об’єкт функції.
# Змінна f зберігає посилання на об’єкт функції square, а не результат її виконання.